        /// <summary>
        /// 中文化艺人名字符串
        /// </summary>
        public static string ChineselizeArtist(string artist)
        {
            if (string.IsNullOrWhiteSpace(artist))
                return string.Empty;

            foreach (var item in ArtistNamePairs)
                if (item.Name.Equals(artist))
                    return item.ChineseName;

            return artist;
        }

        /// <summary>
        /// 中文化艺人名字符串列表
        /// </summary>
        public static void ChineselizeArtists(List<string> artists)
        {
            for (int i = 0; i < artists.Count; i++)
                artists[i] = ChineselizeArtist(artists[i]);
        }

        /// <summary>
        /// 中文化艺人名字符串列表
        /// </summary>
        /// <param name="artists"></param>
        public static void ChineselizeArtists(string[] artists)
        {
            for (int i = 0; i < artists.Length; i++)
                artists[i] = ChineselizeArtist(artists[i]);
        }

        /// <summary>
        /// 中文化艺人名字符串列表
        /// </summary>
        public static List<string> ToChineselizeArtists(this List<string> artists)
        {
            var newList = new List<string>();

            for (int i = 0; i < artists.Count; i++)
                newList.Add(ChineselizeArtist(artists[i]));

            return newList;
        }

        /// <summary>
        /// 中文化艺人名字符串列表
        /// </summary>
        public static string[] ToChineselizeArtists(this string[] artists)
        {
            var newArray = new string[artists.Length];

            for (int i = 0; i < artists.Length; i++)
                newArray[i] = ChineselizeArtist(artists[i]);

            return newArray;
        }

